"""Plugin Manager - Extensible Command System with alias routing"""
from typing import Dict, List, Optional, Any
from dataclasses import dataclass
from loguru import logger

@dataclass
class CommandMetadata:
    name: str
    category: str
    description: str
    patterns: List[str]
    examples: List[str] = None

@dataclass
class CommandResult:
    success: bool
    message: str
    data: Any = None
    error: Optional[str] = None
    
    @classmethod
    def ok(cls, message: str, data: Any = None):
        return cls(success=True, message=message, data=data)
    
    @classmethod
    def error(cls, message: str, error: str = None):
        return cls(success=False, message=message, error=error or message)

class CommandPlugin:
    """Base class for command plugins"""
    metadata: CommandMetadata = None
    # Optional: list of additional action names this plugin handles
    SUPPORTED_ACTIONS: List[str] = []
    
    async def execute(self, entities: Dict[str, Any], context: Dict[str, Any]) -> CommandResult:
        raise NotImplementedError

